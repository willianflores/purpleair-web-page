					<h2>Descrição da rede</h2>
                    <hr>
                        
                        <p>Em junho 2019 se tornou operacional a maior rede de monitoramento da qualidade do ar da Amazônia, baseada em sensores <a href="https://www2.purpleair.com/products/purpleair-pa-ii-sd" target="_blank">PurpleAir PA-II-SD</a> de baixo custo e conceito inovador da Internet das Coisas (Internet of Things - IoT) que se conecta a uma rede internacional, com disponibilização de dados em tempo real e de forma gratuita. Foram instalados 30 sensores, distribuídos nas sedes dos 22 municípios do Estado do Acre. </p>

						<p>Esta ação foi realizada pelo <a href="https://www.mpac.mp.br/" target="_blank">Ministério Público do Estado do Acre</a> em parceria com a <a href="http://www.ufac.br/" target="_blank">Universidade Federal do Acre</a>, Tribunal de Justiça, Prefeitura de Assis Brasil, Secretaria de Meio Ambiente de Tarauacá, Batalhão da Polícia Militar de Capixaba, Instituto Federal do Acre/Campus Tarauacá, Prefeitura de Rodrigues Alves, Prefeitura de Jordão, Prefeitura de Santa Rosa do Purus, Prefeitura de Porto Acre, Defesa Civil de Tarauacá, Prefeitura de Xapuri, Prefeitura de Marechal Thaumaturgo, Instituto Federal do Acre/Sena Madureira, Prefeitura de Santa Rosa do Purus, 8.º Batalhão de Educação, Proteção e Combate a Incêndios Florestais/Urbanos de Xapuri e IMAC/Núcleo de Representação em Feijó/Envira.</p>

						<p></p>
							
					
				<h1>Laboratório de Geoprocessamento Aplicado ao Meio Ambiente - LabGama</h1>                
				<hr class="hr-title">

						<p>O Laboratório de Geoprocessamento Aplicado ao Meio Ambiente (LabGAMA) foi criado em 2013 na Universidade Federal do Acre com a missão de aplicar técnicas e ferramentas de geoprocessamento e sensoriamento remoto para compreender melhor o ambiente em que vivemos.</p>

						<p>Atualmente o LabGAMA vem desenvolvendo, em parceria com diversas instituições, ações relacionadas a Rede de Monitoramento da Qualidade do Ar do Acre. Essas ações estão sendo lideradas pelo Dr. Willian Flores e comtemplam monitoramento e gestão de rede, processamento de dados e produção informação sobre qualidade do ar no Estado do Acre. Neste contexto, essa página web foi construída para divulgação das informações produzidas. Para tanto acesso aba <a href="index.php">Dados de Qualidade do Ar</a> na barra de navegação dessa página web.  </p>

					
